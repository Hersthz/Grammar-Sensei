const db = ctx.GrammarSenseiData.GRAMMAR_DATABASE;

const tokens = [];
for (const e of db) {
  [e.display, e.pattern].forEach((x) => x && tokens.push(x));
  (e.variants || []).forEach((v) => tokens.push(v));
}
const norm = (s) => String(s).replace(/[〜～。、\s（）()]/g, "").replace(/…/g, "");
const normTokens = tokens.map(norm);

// Strip a user entry to its kana core for matching: take part before / or （,
// drop ～ wildcards and notes.
function core(kana) {
  return norm(kana.split("/")[0].split("・")[0].replace(/[（(].*$/, ""));
}

function covered(kana) {
  const c = core(kana);
  if (!c) return true;
  return normTokens.some((t) => t.includes(c) || c.includes(t) && t.length >= 2);
}

